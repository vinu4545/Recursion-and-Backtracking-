
def powerset(arr,i,ans,comb):
    if i >= len(arr):
        comb.append(ans.copy( ))
        return 
    powerset(arr,i+1,ans,comb)   #Exclude
    ans.append(arr[i])
    powerset(arr,i+1,ans,comb)
    ans.pop()
    return comb

arr=[1,2,3]
ans=powerset(arr,0,[],[])
print(ans)


